package ejercicio_4.Entidades;

/**
 *
 * @author droa
 */
public class Rectangulo {
    
    //atributos
    
    private int base;
    private int altura;
    
    // Constructores

    public Rectangulo() {
    }

    public Rectangulo(int base, int altura) {
        this.base = base;
        this.altura = altura;
    }
    
    // Getter and Setter

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
    
    
    
}
